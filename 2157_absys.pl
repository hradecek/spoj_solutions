#!/usr/bin/perl

chomp($t = <>);
while($t) {
    $_ = <>;
    next if /^\n/; --$t;
    @_ = /(\w+)\s*\+\s*(\w+)\s*=\s*(\w+)/;
    if($_[0] =~ /machula/) {
        $_[0] = ($_[2] - $_[1]);
    } elsif($_[1] =~ /machula/) {
        $_[1] = ($_[2] - $_[0]);
    } else {
        $_[2] = ($_[0] + $_[1]);
    }
    print "$_[0] + $_[1] = $_[2]\n";
}

